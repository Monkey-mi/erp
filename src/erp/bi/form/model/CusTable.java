package erp.bi.form.model;

public class CusTable {
	private String ft_id;//表id
	private String code;//表名
	private String type;//表类型
	private String key;//主外键
	public String getFt_id() {
		return ft_id;
	}
	public void setFt_id(String ft_id) {
		this.ft_id = ft_id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	
	
}
