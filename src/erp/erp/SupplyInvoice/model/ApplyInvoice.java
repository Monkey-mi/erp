package erp.erp.SupplyInvoice.model;

public class ApplyInvoice {
	private double sqbh;
	private double sqxh;
	private double sqje;
	private double yfje;
	private String fplb;
	private String fphm;
	public double getSqbh() {
		return sqbh;
	}
	public void setSqbh(double sqbh) {
		this.sqbh = sqbh;
	}
	public double getSqxh() {
		return sqxh;
	}
	public void setSqxh(double sqxh) {
		this.sqxh = sqxh;
	}
	public double getSqje() {
		return sqje;
	}
	public void setSqje(double sqje) {
		this.sqje = sqje;
	}
	public double getYfje() {
		return yfje;
	}
	public void setYfje(double yfje) {
		this.yfje = yfje;
	}
	public String getFplb() {
		return fplb;
	}
	public void setFplb(String fplb) {
		this.fplb = fplb;
	}
	public String getFphm() {
		return fphm;
	}
	public void setFphm(String fphm) {
		this.fphm = fphm;
	}
	
}
