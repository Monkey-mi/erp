package erp.erp.master.vendorfiles.model;

import java.util.Date;

public class HistoryVendorFiles {
	private String			csbh;
	private double			jlxh;
	private String			csmc;
	private Date			czsj;
	private String			czym;

	public String getCsbh() {
		return csbh;
	}
	public void setCsbh(String csbh) {
		this.csbh = csbh;
	}
	public double getJlxh() {
		return jlxh;
	}
	public void setJlxh(double jlxh) {
		this.jlxh = jlxh;
	}
	public String getCsmc() {
		return csmc;
	}
	public void setCsmc(String csmc) {
		this.csmc = csmc;
	}
	public Date getCzsj() {
		return czsj;
	}
	public void setCzsj(Date czsj) {
		this.czsj = czsj;
	}
	public String getCzym() {
		return czym;
	}
	public void setCzym(String czym) {
		this.czym = czym;
	}
}
