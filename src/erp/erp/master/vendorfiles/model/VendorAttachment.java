package erp.erp.master.vendorfiles.model;

import java.util.Date;

public class VendorAttachment {
	private String			csbh;
	private double			wjbh;
	private String			wjmc;
	private String			wjlj;
	private Date			wjrq;
	private String			cjrm;

	public String getCsbh() {
		return csbh;
	}
	public void setCsbh(String csbh) {
		this.csbh = csbh;
	}
	public double getWjbh() {
		return wjbh;
	}
	public void setWjbh(double wjbh) {
		this.wjbh = wjbh;
	}
	public String getWjmc() {
		return wjmc;
	}
	public void setWjmc(String wjmc) {
		this.wjmc = wjmc;
	}
	public String getWjlj() {
		return wjlj;
	}
	public void setWjlj(String wjlj) {
		this.wjlj = wjlj;
	}
	public Date getWjrq() {
		return wjrq;
	}
	public void setWjrq(Date wjrq) {
		this.wjrq = wjrq;
	}
	public String getCjrm() {
		return cjrm;
	}
	public void setCjrm(String cjrm) {
		this.cjrm = cjrm;
	}
}
