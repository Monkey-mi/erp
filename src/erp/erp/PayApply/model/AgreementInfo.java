package erp.erp.PayApply.model;

public class AgreementInfo {
	private double			htbh;
	private String			csbh;
	private String			csmc;
	private double			htze;
	private double			sqze;
	private double			kspzq;

	public double getHtbh() {
		return htbh;
	}
	public void setHtbh(double htbh) {
		this.htbh = htbh;
	}
	public String getCsbh() {
		return csbh;
	}
	public void setCsbh(String csbh) {
		this.csbh = csbh;
	}
	public String getCsmc() {
		return csmc;
	}
	public void setCsmc(String csmc) {
		this.csmc = csmc;
	}
	public double getHtze() {
		return htze;
	}
	public void setHtze(double htze) {
		this.htze = htze;
	}
	public double getSqze() {
		return sqze;
	}
	public void setSqze(double sqze) {
		this.sqze = sqze;
	}
	public double getKspzq() {
		return kspzq;
	}
	public void setKspzq(double kspzq) {
		this.kspzq = kspzq;
	}
}
